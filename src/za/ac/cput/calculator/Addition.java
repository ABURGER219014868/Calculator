package za.ac.cput.calculator;

public class Addition {
    /**
     * This adds two integers
     * @param a = a
     * @param b = b
     * @return a+b
     */
    public static int integerSum(int a, int b){
        return a + b;
    }

    /**
     * This adds two real numbers
     * @param a
     * @param b
     * @return
     */
    public static double doubleSum(double a, double b){
        return a + b;
    }

}
